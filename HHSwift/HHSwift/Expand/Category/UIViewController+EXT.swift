import Foundation
import UIKit

extension UIViewController {
    
    /*
     //优点：
     1.Category是非常实用的扩展机制，可以很方便的为一个已有的类添加属性或者方法，而不需要笨拙的去继承。
     2.运行时多态的概念，可以让一个类的对象动态的以其他类行为去执行（OC中多了很多运行时态的机制，其中id的特殊用途，可以通过id类型的变量，调用不同类的同名函数，即使这些类没有任何关系）。
     3.OC中类没有所谓的构造函数和析构函数，所以如果一个类中有其他类的数据（动态申请的），那么释放内存就不能依赖析构函数，但可以采用重写-(id)
     free来实现类中所有数据释放。
     4.OC中成员变量也有三种访问权限，@public,@protected,@private。但默认的是@protected，而C++中默认是private。
     5.OC中成员函数只有两种，一种是实例函数，一种是类函数。
     
     //缺点：
     1.不支持多重继承，只有多级继承。
     2.使用动态运行时类型，所有的方法都是函数调用，所以很多编译时优化方法都用不到。（如内联函数等），性能低劣。
     3.不支持运算符重载
     4.程序执行效率：汇编>C>OC
     */
}


		
